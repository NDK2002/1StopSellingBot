
from fastapi import WebSocket

class ConnectionManager:
    def __init__(self):
        # We store connections by session_id
        # In a real distributed system (Redis auto-scaling), we would use Redis Pub/Sub here.
        # But for this Phase 3 we use in-memory dict mapped to session_id.
        # A single session_id can have multiple connections (user, admin(s)).
        self.active_connections: dict[str, list[WebSocket]] = {}

    async def connect(self, websocket: WebSocket, session_id: str):
        await websocket.accept()
        if session_id not in self.active_connections:
            self.active_connections[session_id] = []
        self.active_connections[session_id].append(websocket)

    def disconnect(self, websocket: WebSocket, session_id: str):
        if session_id in self.active_connections:
            if websocket in self.active_connections[session_id]:
                self.active_connections[session_id].remove(websocket)
            if not self.active_connections[session_id]:
                del self.active_connections[session_id]

    async def broadcast_to_session(self, session_id: str, message: dict):
        if session_id in self.active_connections:
            for connection in self.active_connections[session_id]:
                try:
                    await connection.send_json(message)
                except Exception:
                    pass # Connection might be dead

manager = ConnectionManager()
